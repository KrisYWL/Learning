       PORT EQU 0CFA0H
       ORG 0000H
       LJMP START
       ORG 0100H
START: MOV A,#11H
       MOV R4,#03H
       ACALL DISP+0
LOOP1: ACALL LED
DJNZ R4,LOOP1
LLL:   MOV A,#12H
       MOV R4,#10
       ACALL DISP
LOOP2: ACALL LED
DJNZ R4,LOOP2
       MOV A,#10H
       ACALL DISP
       MOV R4,#03H
TTT:   MOV A,#1CH
       ACALL DISP
       ACALL LED
       MOV A,#10H
       ACALL DISP
       ACALL DE02S
      DJNZ R4,TTT
       MOV A,#12H
       ACALL DISP
       MOV R4,#10
                 
LOOP3:ACALL LED
       DJNZ R4,LOOP3
       MOV A,#21H
       ACALL DISP
       MOV R4,#03H
GGG:   MOV A,#0C1H
       ACALL DISP
       ACALL LED
       MOV A,#01H
       ACALL DISP
       ACALL DE02S
       DJNZ R4,GGG
       LJMP LLL
DE10S: MOV R5,#100
       LJMP DE1
DE3S:  MOV R5,#30
       LJMP DE1
DE1S:  MOV R5,#10
       LJMP DE1
DE02S: MOV R5,#02
DE1:   MOV R6,#200
DE2:   MOV R7,#126
DE3:   DJNZ R7,DE3
       DJNZ R6,DE2
       DJNZ R5,DE1
       RET
DISP:  MOV DPTR,#PORT
       CPL A
       MOVX @DPTR,A
       RET
LED:MOV A,R4
LCALL TABLE
MOV P1,A
LCALL DE1S
RET
TABLE:MOV DPTR,#DMZ
MOVC A,@A+DPTR
RET
DMZ:DB 40H,3FH,06H,5BH,4FH,66H
DB 6DH,7DH,07H,7FH,6FH
       END